"""
In Python:

The type of a variable is determined at runtime based on the value assigned to it.
A variable can be reassigned to a different type at any time.
"""

# Example 1: Assigning an integer
x = 10
print(x)           # Output: 10
print(type(x))     # Output: <class 'int'>

# Example 2: Reassigning a string
x = "Hello"
print(x)           # Output: Hello
print(type(x))     # Output: <class 'str'>

# Example 3: Reassigning a float
x = 3.14
print(x)           # Output: 3.14
print(type(x))     # Output: <class 'float'>

