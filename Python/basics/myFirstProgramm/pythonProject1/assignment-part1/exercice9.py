"""
Exercise 9: Set Operations

Create two sets:
set_a = {1, 2, 3, 4, 5}
set_b = {4, 5, 6, 7, 8}
Perform and print the following operations:
Union of set_a and set_b
Intersection of set_a and set_b
Difference between set_a and set_b
Symmetric difference between set_a and set_b
"""
set_a = {1, 2, 3, 4, 5}
set_b = {4, 5, 6, 7, 8}
print("Union set_a and set_b",set_a | set_b)
print("Intersection of set_a and set_b",set_a & set_b)
print("Difference between set_a and set_b",set_a - set_b)
print("Symmetric difference between set_a and set_b",set_a ^ set_b)

